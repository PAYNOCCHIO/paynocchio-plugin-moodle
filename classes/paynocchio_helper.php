    /**
     * Main cURL worker
     * @param string $method
     * @param string $url
     * @param string $body
     * @param bool $simple
     * @return array
     * @throws \dml_exception
     */
    public function sendRequest(string $method, string $url, string $body = "", bool $simple = false): array {
        $headers = [
            'X-API-KEY: X-API-KEY',
            'Content-Type: application/json'
        ];

        $test_mode = get_config('paygw_paynocchio', 'testmode') ? 'on' : 'off';

        $headers[] = 'X-TEST-MODE-SWITCH:'. $test_mode;

        if($simple) {
            $headers[] = 'X-Company-Signature:'. $this->simpleSignature;
        } else {
            $headers[] = 'X-Wallet-Signature:'. $this->signature;
        }

        $curl = curl_init();

        curl_setopt_array($curl, array(
            CURLOPT_URL => $this->baseUrl . $url,
            CURLOPT_RETURNTRANSFER => true,
            CURLOPT_ENCODING => '',
            CURLOPT_MAXREDIRS => 10,
            CURLOPT_TIMEOUT => 0,
            CURLOPT_FOLLOWLOCATION => true,
            CURLOPT_HTTP_VERSION => CURL_HTTP_VERSION_1_1,
            CURLOPT_CUSTOMREQUEST => $method,
            CURLOPT_POSTFIELDS => $body,
            CURLOPT_USERAGENT => 'Mozilla/4.0 (compatible; MSIE 6.0; Windows NT 5.1; .NET CLR 1.1.4322)',
            CURLOPT_HTTPHEADER => $headers,
        ));

        $response = curl_exec($curl);
        $httpCode = curl_getinfo($curl, CURLINFO_HTTP_CODE);

        curl_close($curl);

        return [
            'status_code' => $httpCode,
            'response' => $response,
        ];
    }

