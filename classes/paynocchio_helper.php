    /**
     *  X-Wallet-Signature
     */
    public function createSignature(): bool|string
    {
        return hash("sha256", $this->secret . "|" . $this->envId . "|" . $this->userId);
    }
    /**
     *  X-Company-Signature
     */
    public function createSimpleSignature(): bool|string
    {
        return hash("sha256", $this->secret . "|" . $this->envId);
    }

