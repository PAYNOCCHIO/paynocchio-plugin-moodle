    /**
     * Currency filter
     */
    public function filterCurrencyByCode(string $code)
    {
        $wallet_currencies = $this->getCurrencies();
        if($wallet_currencies['status_code'] === 200) {
            $json_response = json_decode($wallet_currencies['response']);
            $currency = array_filter($json_response->currencies, fn($x) => $x->alphabetic_code === $code);
            return array_shift($currency);
        } else {
            return (object) array('uuid' => '970d83de-1dce-47bd-a45b-bb92bf6df964', 'code' => 'USD');
        }
    }

    /**
     * Transform and merge identical rules
     * @param $data
     * @return array|null
     */
    public function transformRewardingRules($data)
    {
    $result = [];

    if ($data) {
        foreach ($data as $item) {
            $existing = null;

            foreach ($result as &$el) {
                if ($el->operation_type === $item->operation_type &&
                    $el->min_amount === $item->min_amount &&
                    $el->max_amount === $item->max_amount) {
                    $existing = &$el;
                    break;
                }
            }

            if ($existing) {
                $existing['value'] += $item->value;
            } else {
                $result[] = $item;
            }
        }

        return $result;
    }

    return null;
    }

    /**
     * Calculate Rewarding rule for static data
     * @param $sum
     * @param $operationType
     * @return array
     */
    public function getCurrentRewardRule($sum, $operationType) {
        $wallet_structure = $this->envStructure;
        $obj = $this->transformRewardingRules($wallet_structure['rewarding_group']->rewarding_rules);
        $totalValue = 0;
        $minAmount = INF;
        $maxAmount = -INF;
        $value_type = null;
        $conversion_rate = 1;

        if ($obj) {
            foreach ($obj as $item) {
                $conversion_rate = $item->conversion_rate;
                $value_type = $item->value_type;
                if ($item->operation_type === $operationType && $sum >= $item->min_amount && $sum <= $item->max_amount) {
                    $totalValue += $item->value;
                    if ($item->min_amount < $minAmount) {
                        $minAmount = $item->min_amount;
                    }
                    if ($item->max_amount > $maxAmount) {
                        $maxAmount = $item->max_amount;
                    }
                }
            }
        }

        return [
            'totalValue' => $value_type === 'percentage' ? $totalValue / $conversion_rate / 100 : $totalValue,
            'minAmount' => $minAmount,
            'maxAmount' => $maxAmount,
            'value_type' => $value_type,
            'conversion_rate' => $conversion_rate,
            'operationType' => $operationType,
            'bonus_conversion_rate' => $wallet_structure['bonus_conversion_rate'],
        ];
    }

    /**
     * Calculated Rewarding and Commission
     * @param float $sum
     * @param string $operationType
     * @return array
     */
    public function calculateRewardsAndCommissions(float $sum, string $operationType): array
    {
        $rules = $this->getCurrentRewardRule($sum, $operationType);
        $conversion_rate_when_payment = $rules['bonus_conversion_rate'] ?: 1;
        $commission = $this->calculateCommissionForAmount($sum);
        $sum_with_commission = $sum + $commission;
        $sum_without_commission = $sum - $commission;

        if($rules['value_type'] === "percentage") {
            $bonuses_to_get = intval($sum_without_commission * $rules['totalValue']);
        } else {
            $bonuses_to_get = $rules['totalValue'];
        }

        return [
          'bonuses_to_get' => $bonuses_to_get,
          'bonuses_in_dollar' => round($bonuses_to_get * $conversion_rate_when_payment, 2),
          'commission' => $commission,
          'sum_without_commission' => round($sum_without_commission, 2),
          'sum_with_commission' => round($sum_with_commission, 2),
          'sum_with_commission_minus_commission' => round($sum_with_commission - $commission, 2),
        ];

    }
    /**
     * Calculate commission
     * @param {float} amount
     */
    public function calculateCommissionForAmount($amount): float
    {
        $wallet_structure = $this->envStructure;
        $wallet_percentage_commission = $wallet_structure['wallet_percentage_commission'] ?? 0;
        $wallet_fixed_commission = $wallet_structure['wallet_fixed_commission'] ?? 0;
        $magic_cent = $wallet_structure['magic_cent'] ?? 0;

        return round(($amount * ($wallet_percentage_commission / 100) + $wallet_fixed_commission) + $magic_cent, 2);
        //return (floor($commission * 100) / 100) + $magic_cent;
    }

    /**
     * Filter the rewarding groups array to return only active and not expired campaigns
     * @param $groups
     * @return array|null
     */

    static function filterEnvRewardingGroups($groups): ?array
    {
        if(!empty($groups)) {
            return array_values(array_filter($groups, [__CLASS__, 'checkFilter']));
        }
        return null;
    }

    /**
     * Filter rule for campaign filtering
     * @param $group
     * @return bool|null
     */
    static function checkFilter($group): ?bool
    {
        if(empty($group->rewarding_rules)) {
            return null;
        }
        return $group->active && strtotime($group->date_from) <= time() && strtotime($group->date_to) >= time();
    }

